      <p><h2>Joy Elbaz Lassier-Capon</h2>
                <p>02/10/1988 - 02/10/1988</p>
                <p>Doctorante et enseignante en histoire de la philosophie ancienne<br>Institut d’Histoire de la Philosophie (UR 3276)</p>
                <p>J’interroge les prolongements contemporains des implications de l’exigence de non-contradiction formulée par Aristote sur la rationalité pratique. Outre les apports de la non-contradiction à la réflexion sur les dilemmes moraux et son actualité en philosophie de l’action et en philosophie morale, mes recherches abordent la théorie dispositionnelle de la vertu (Lewis). Le cadre unificateur de mes travaux est l’étude du critère de cohérence de la rationalité pratique. Il va de soi que cette perspective implique que mes recherches traitent également de métaéthique et de normativité. En particulier, elles s’intéressent à l’agentivité, à la question de la deep rationality, et à la structure du raisonnement et des formes du discours à l’œuvre en philosophie morale (prescriptivisme).</p></p>
